package com.gzcjteam.shundai.utils;


public class getUserInfo{	
	private String id;
	private String phone;
	private String password;
	private String nick;
	private String head_pic_url;
	private String rank;
	private String identity_status;
	private String register_time;
	private String school_name;
	private String floor_address;
		
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getPhone() {
		return phone;
	}
	public void setPhone(String phone) {
		this.phone = phone;
	}
	public String getPassword() {
		return password;
	}
	public void setPassword(String password) {
		this.password = password;
	}
	public String getNick() {
		return nick;
	}
	public void setNick(String nick) {
		this.nick = nick;
	}
	public String getHead_pic_url() {
		return head_pic_url;
	}
	public void setHead_pic_url(String head_pic_url) {
		this.head_pic_url = head_pic_url;
	}
	public String getRank() {
		return rank;
	}
	public void setRank(String rank) {
		this.rank = rank;
	}
	public String getIdentity_status() {
		return identity_status;
	}
	public void setIdentity_status(String identity_status) {
		this.identity_status = identity_status;
	}
	public String getRegister_time() {
		return register_time;
	}
	public void setRegister_time(String register_time) {
		this.register_time = register_time;
	}
	public String getSchool_name() {
		return school_name;
	}
	public void setSchool_name(String school_name) {
		this.school_name = school_name;
	}
	public String getFloor_address() {
		return floor_address;
	}
	public void setFloor_address(String floor_address) {
		this.floor_address = floor_address;
	}
}
